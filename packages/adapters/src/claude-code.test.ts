import { InMemoryFileSystem } from '@sentiness/_test-utils';
import { describe, expect, it } from 'vitest';
import { claudeCodeAdapter } from './claude-code.js';
import type { RenderOptions } from './types.js';

const options: RenderOptions = {
  sentinessVersion: '0.1.0',
  configPath: 'sentiness.config.json',
  baselinePath: '.sentiness/baseline.json',
  pendingPath: '.sentiness/pending-feedback.json',
};

describe('claudeCodeAdapter', () => {
  it('creates a managed section in CLAUDE.md', async () => {
    const fs = new InMemoryFileSystem();
    const result = await claudeCodeAdapter.install('/project', fs, options);

    expect(result).toEqual({
      agent: 'claude-code',
      targetPath: '/project/CLAUDE.md',
      changed: true,
    });

    const content = await fs.readFile('/project/CLAUDE.md');
    expect(content).toContain('<!-- sentiness:start -->');
    expect(content).toContain('<!-- generated by @sentiness/adapters');
    expect(content).toContain('<!-- sentiness:end -->');
  });

  it('replaces only the existing managed section and is idempotent', async () => {
    const fs = new InMemoryFileSystem({
      '/project/CLAUDE.md':
        '# Existing instructions\n\n<!-- sentiness:start -->\nold\n<!-- sentiness:end -->\n\nKeep me.\n',
    });

    const first = await claudeCodeAdapter.install('/project', fs, options);
    const second = await claudeCodeAdapter.install('/project', fs, options);
    const content = await fs.readFile('/project/CLAUDE.md');

    expect(first.changed).toBe(true);
    expect(second.changed).toBe(false);
    expect(content).toContain('# Existing instructions');
    expect(content).toContain('Keep me.');
    expect(content).not.toContain('\nold\n');
    expect(content.match(/<!-- sentiness:start -->/g)).toHaveLength(1);
  });

  it('rejects malformed managed markers', async () => {
    const fs = new InMemoryFileSystem({
      '/project/CLAUDE.md': '# Existing\n\n<!-- sentiness:start -->\nmissing end\n',
    });

    await expect(claudeCodeAdapter.install('/project', fs, options)).rejects.toThrow(
      'Invalid Sentiness managed section markers in /project/CLAUDE.md',
    );
  });
});
